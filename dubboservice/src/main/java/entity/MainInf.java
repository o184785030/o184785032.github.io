package entity;

public class MainInf {
	private int id;
	private int goodsId;
	private int saleKind;
	private int kind;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(int goodsId) {
		this.goodsId = goodsId;
	}
	public int getSaleKind() {
		return saleKind;
	}
	public void setSaleKind(int saleKind) {
		this.saleKind = saleKind;
	}
	public int getKind() {
		return kind;
	}
	public void setKind(int kind) {
		this.kind = kind;
	}
	
}
